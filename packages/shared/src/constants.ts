/** Сколько клеток волна может захватить за один тик (скорость фронта). */
export const WAVE_SPEED_CELLS_PER_TICK = 30;

/**
 * Направленность волны (0..1). Приоритет клетки в очереди =
 *   глубина_от_границы + WAVE_DIRECTION_BIAS × расстояние_до_цели.
 *
 * Глубина заставляет фронт расти слоями (толсто, округло), а bias
 * решает, какая часть фронта бежит к цели быстрее.
 *   0   → идеально круглая волна во все стороны (направление игнорируется);
 *   0.6 → к цели фронт уходит вчетверо дальше, чем в стороны — «капля»;
 *   <1  → нити-щупальца шириной в клетку МАТЕМАТИЧЕСКИ невозможны.
 * Держим строго < 1. Это тот параметр, что чинит «шарики на ниточках».
 */
export const WAVE_DIRECTION_BIAS = 0.6;

